// The loop statement is used for logging even numbers from 1 to 10 to the console

// The let i = 1 statement initializes the variable i to 1.
// The for loop runs as long as the variable i is less than or equal to 10.
// The i variable is incremented by 1 in each iteration.
for (let i = 1; i <= 10; i++) {
  // The if statement checks if the number is even by using the modulo operator.
  if (i % 2 === 0) {
    // If the number is even, it is logged to the console.
    console.log(i);
  }
}
